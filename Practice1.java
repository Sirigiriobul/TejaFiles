package allPractice;

public class Practice1 {
public static void main(String[] args) {
	int number=5;
	int factors=0;
	for (int i=1;i<number;i++)
	{
        factors++;
	}
if (factors==2)
{
	System.out.println(number+" is a prime number");}
else
{
	System.out.println(number+" is not a prime number");
	}
}
	
	
	
}

